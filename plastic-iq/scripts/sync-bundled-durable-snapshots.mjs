#!/usr/bin/env node
/**
 * Sync data/durable-published approved snapshots → src/lib/apr/durable-approved (browser bundle).
 */
import { copyFileSync, mkdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs'
import { join } from 'node:path'

const root = process.cwd()
const src = join(root, 'data', 'durable-published', 'approved-snapshots')
const dest = join(root, 'src', 'lib', 'apr', 'durable-approved')

const SLUG_BY_PRODUCT = {
  '1cf2fa4e-5cdd-4798-8f3c-6c273ae69fa8': 'lodge-approved.json',
  'c645ae86-0b82-429d-8f46-78b8007041b5': 'all-clad-approved.json',
  'e451b158-6094-44f9-9628-a2a25974482e': 'caraway-approved.json',
  '7a457a86-ab62-4cbf-90b9-ccaeafe06896': 't-fal-approved.json',
}

mkdirSync(dest, { recursive: true })

const index = JSON.parse(readFileSync(join(src, 'index.json'), 'utf8'))
copyFileSync(join(src, 'index.json'), join(dest, 'manifest.json'))

const bundledEntries = []
for (const [productId, snapshotId] of Object.entries(index.latest_by_product)) {
  const slug = SLUG_BY_PRODUCT[productId]
  if (!slug) continue
  const recordPath = join(src, 'records', `${snapshotId}.json`)
  if (!existsSync(recordPath)) {
    throw new Error(`Missing approved snapshot record: ${recordPath}`)
  }
  copyFileSync(recordPath, join(dest, slug))
  bundledEntries.push({ productId, slug, exportName: slugToExport(slug) })
}

writeFileSync(
  join(dest, 'README.md'),
  `# Bundled durable-approved snapshots\n\nAuto-synced from \`data/durable-published/\` via \`npm run sync:bundled-durable\`.\nBrowser ProductPage reads bundledSnapshots.generated.ts — do not edit by hand.\n`,
)

const imports = bundledEntries
  .map((e) => `import ${e.exportName} from './${e.slug.replace('.json', '')}.json' with { type: 'json' }`)
  .join('\n')

const exports = bundledEntries
  .map((e) => `export const ${e.exportName}_STORED = ${e.exportName} as StoredApprovedSnapshotVersion`)
  .join('\n')

const byProduct = bundledEntries
  .map((e) => `  ['${e.productId}']: ${e.exportName}_STORED,`)
  .join('\n')

const generatedTs = `/** Auto-generated by npm run sync:bundled-durable — do not edit. */
import type { StoredApprovedSnapshotVersion } from '../durable/durableStoreTypes'
${imports ? imports + '\n' : ''}
export const BUNDLED_MANIFEST = ${JSON.stringify({ latest_by_product: index.latest_by_product }, null, 2)} as const

${exports}

export const BUNDLED_BY_PRODUCT: Record<string, StoredApprovedSnapshotVersion> = {
${byProduct}
}
`
writeFileSync(join(root, 'src', 'lib', 'apr', 'durable-approved', 'bundledSnapshots.generated.ts'), generatedTs)

console.log(`✓ synced bundled durable-approved snapshots → ${dest}`)

function slugToExport(slug) {
  return slug
    .replace('.json', '')
    .replace(/-([a-z])/g, (_, c) => c.toUpperCase())
    .replace(/^([a-z])/, (_, c) => c.toUpperCase())
    .replace(/Approved$/, 'Approved')
}
